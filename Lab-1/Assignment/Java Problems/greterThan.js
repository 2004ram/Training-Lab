// Question: Write a function isGreaterThanFive that takes a number as an argument and returns true if the number is greater than 5, otherwise false.

function isGreaterThanFive(num) {
    return num > 5;
}

console.log(isGreaterThanFive(8));  // Output: true
console.log(isGreaterThanFive(3));  // Output: false
